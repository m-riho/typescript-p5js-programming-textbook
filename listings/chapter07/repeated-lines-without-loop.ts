// p5.jsの機能を取り込む
import p5 from "p5";

// p5.js全体の処理をまとめる関数
const sketch = (p: p5): void => {
    // 最初に一度だけ実行される処理
    p.setup = (): void => {
        // キャンバスを作り、背景を明るい灰色で塗る
        p.createCanvas(320, 220);
        p.background(245);

        // 線の色を指定する
        p.stroke(40);

        // 同じ形の線を、x 座標だけ変えて何本も描く
        p.line(30, 30, 30, 190);
        p.line(60, 30, 60, 190);
        p.line(90, 30, 90, 190);
        p.line(120, 30, 120, 190);
        p.line(150, 30, 150, 190);
        p.line(180, 30, 180, 190);
        p.line(210, 30, 210, 190);
        p.line(240, 30, 240, 190);
        p.line(270, 30, 270, 190);
    };
};

new p5(sketch);
