// p5.jsの機能を取り込む
import p5 from "p5";

// p5.js全体の処理をまとめる関数
const sketch = (p: p5): void => {
    // 最初に一度だけ実行される処理
    p.setup = (): void => {
        p.createCanvas(400, 400);  // 作成するキャンバスの幅と高さを指定する
        p.background(230, 240, 255);  // キャンバスを薄い水色で塗りつぶす

        p.stroke(60);  // 線の色を暗い灰色にする
        p.strokeWeight(4);  // 線の太さを4ピクセルにする

        p.fill(255, 220, 140);  // 顔の色を薄い黄色にする
        p.circle(200, 190, 220);  // 顔の輪郭になる大きな円を描く

        p.fill(40);  // 目の色を暗い灰色にする
        p.circle(155, 160, 35);  // 左目になる小さな円を描く
        p.circle(245, 160, 35);  // 右目になる小さな円を描く

        p.fill(255, 160, 120);  // 鼻の色を薄い赤にする
        p.triangle(200, 185, 180, 225, 220, 225);  // 鼻になる三角形を描く

        p.stroke(120, 60, 80);  // 口の線の色を赤紫にする
        p.strokeWeight(6);  // 口の線を太くする
        p.line(155, 260, 245, 260);  // 口になる線を描く
    };
};

new p5(sketch);
