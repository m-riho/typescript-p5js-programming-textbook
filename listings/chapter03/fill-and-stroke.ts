// p5.jsの機能を取り込む
import p5 from "p5";

// p5.js全体の処理をまとめる関数
const sketch = (p: p5): void => {
    // 最初に一度だけ実行される処理
    p.setup = (): void => {
        p.createCanvas(420, 220);  // 作成するキャンバスの幅と高さを指定する
        p.background(250);  // キャンバスを灰色で塗りつぶす

        p.stroke(30, 90, 180);  // 線の色を青にする
        p.strokeWeight(6);  // 線の太さを6ピクセルにする
        p.fill(255, 210, 80);  // 塗りつぶし色を黄色にする
        p.circle(130, 110, 120);  // 青い線と黄色い塗りの円を描く

        p.stroke(180, 40, 70);  // 線の色を赤にする
        p.strokeWeight(12);  // 線の太さを12ピクセルにする
        p.fill(120, 210, 170);  // 塗りつぶし色を緑にする
        p.rect(230, 50, 120, 120);  // 赤い線と緑の塗りの正方形を描く
    };
};

new p5(sketch);
